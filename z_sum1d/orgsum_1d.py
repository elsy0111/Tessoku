arr = [3, 4, 5, 6, 9, 10, 13, 14, 15, 16, 17, 18, 19, 20, 23, 27, 28, 29, 30, 32, 33, 34, 35, 36, 39, 45, 46, 48, 53, 54, 55, 56, 57, 59, 60, 61, 62, 63, 64, 70, 71, 74, 75, 76, 77, 79, 80, 81, 85, 87, 89, 90, 91, 94, 95, 99, 100]
def sum_1d(arr):
    n = len(arr)
    cum_sum = [0 for _ in range(n+2)]
    for i in range(1,n + 1):
        cum_sum[i] = cum_sum[i - 1] + arr[i-1]
    return cum_sum
print(sum_1d(arr))
